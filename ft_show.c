#include <stdio.h>

void	 ft_show(int **tab)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (x < 9)
	{
		y = 0;
		while (y < 9)
		{
			printf("%d ", tab[x][y]);
			y++;
		}
		x++;
		printf("\n");
	}
}
